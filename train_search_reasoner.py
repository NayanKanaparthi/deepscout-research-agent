def parse_model_output(text: str) -> dict:
    """
    Parse a model's generated text for validity metrics.
    Returns dict with json_valid, schema_valid, parsed_answer.
    """
    result = {
        "json_valid": False,
        "schema_valid": False,
        "parsed_answer": None,
        "has_think_tags": "<think>" in text and "</think>" in text,
    }

    after_think = text
    if "</think>" in text:
        after_think = text.split("</think>", 1)[1].strip()

    json_match = re.search(r"\{[\s\S]*\}", after_think)
    if not json_match:
        return result

    try:
        parsed = json.loads(json_match.group())
        result["json_valid"] = True
    except json.JSONDecodeError:
        return result

    if "answer" in parsed and "best_result_index" in parsed:
        result["schema_valid"] = True
        answer = str(parsed["answer"]).strip().upper()
        if answer in ("A", "B", "C", "D"):
            result["parsed_answer"] = answer

    return result


@torch.no_grad()
def run_inference_eval(
    model,
    tokenizer,
    eval_dataset,
    max_examples: int = 50,
    max_new_tokens: int = 1024,
    desc: str = "eval",
) -> tuple[dict, list[dict]]:
    """
    Run generation-based evaluation on the eval split.
    Returns aggregate metrics and per-example results.
    """
    model.eval()
    examples = list(eval_dataset)
    if max_examples and len(examples) > max_examples:
        examples = examples[:max_examples]

    all_results = []
    print(f"\n  Running {desc} inference on {len(examples)} examples...")

    for i, example in enumerate(examples):
        if (i + 1) % 10 == 0:
            print(f"    {i + 1}/{len(examples)}...")

        messages = [
            {"role": "system", "content": SEARCH_REASONER_SYSTEM_PROMPT},
            {"role": "user", "content": f"## Question\n{example['question']}\n\n## Search Results\n{example['search_context']}"},
        ]

        try:
            input_text = tokenizer.apply_chat_template(
                messages, tokenize=False, add_generation_prompt=True
            )
        except Exception:
            input_text = tokenizer.apply_chat_template(
                messages, tokenize=False
            )

        inputs = tokenizer(
            input_text, return_tensors="pt", truncation=True, max_length=3072
        ).to(model.device)

        outputs = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            temperature=0.01,
            do_sample=True,
            top_p=0.95,
        )

        generated_ids = outputs[0][inputs["input_ids"].shape[1]:]
        generated_text = tokenizer.decode(generated_ids, skip_special_tokens=False)

        parsed = parse_model_output(generated_text)
        parsed["expected_answer"] = example.get("parsed_answer", "")
        parsed["answer_correct"] = (
            parsed["parsed_answer"] == parsed["expected_answer"]
            if parsed["parsed_answer"]
            else False
        )
        parsed["question_snippet"] = example["question"][:100]
        parsed["generated_snippet"] = generated_text[:300]
        all_results.append(parsed)

    n = len(all_results)
    metrics = {
        f"inference_eval/json_valid_rate_{desc}": sum(r["json_valid"] for r in all_results) / max(n, 1),
        f"inference_eval/schema_valid_rate_{desc}": sum(r["schema_valid"] for r in all_results) / max(n, 1),
        f"inference_eval/answer_accuracy_{desc}": sum(r["answer_correct"] for r in all_results) / max(n, 1),
        f"inference_eval/think_tag_rate_{desc}": sum(r["has_think_tags"] for r in all_results) / max(n, 1),
        f"inference_eval/n_examples_{desc}": n,
    }

    print(f"\n  ── {desc} Results ({n} examples) ──")
    print(f"    JSON valid rate:    {metrics[f'inference_eval/json_valid_rate_{desc}']:.1%}")
    print(f"    Schema valid rate:  {metrics[f'inference_eval/schema_valid_rate_{desc}']:.1%}")
    print(f"    Answer accuracy:    {metrics[f'inference_eval/answer_accuracy_{desc}']:.1%}")
    print(f"    Think tag rate:     {metrics[f'inference_eval/think_tag_rate_{desc}']:.1%}")

    return metrics, all_results


def log_eval_to_wandb(metrics: dict, per_example: list[dict], phase: str):
    """Log eval results to W&B with tables."""
    import wandb

    wandb.log(metrics)

    table = wandb.Table(
        columns=[
            "idx", "question", "json_valid", "schema_valid",
            "parsed_answer", "expected_answer", "correct", "generated_snippet",
        ]
    )
    for i, r in enumerate(per_example):
        table.add_data(
            i,
            r.get("question_snippet", ""),
            r["json_valid"],
            r["schema_valid"],
            r.get("parsed_answer", ""),
            r.get("expected_answer", ""),
            r.get("answer_correct", False),
            r.get("generated_snippet", ""),
        )
    wandb.log({f"{phase}/per_example_results": table})


