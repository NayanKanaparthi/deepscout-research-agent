def load_cot_dataset(path: str) -> Dataset:
    """Load the CoT training JSONL into a HuggingFace Dataset."""
    records = []
    with open(path, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                records.append(json.loads(line))
            except json.JSONDecodeError:
                pass
    print(f"  Loaded {len(records)} records from {path}")
    return Dataset.from_list(records)


def format_to_messages(example: dict) -> dict:
    """Convert a CoT record into chat messages for the tokenizer."""
    user_content = f"## Question\n{example['question']}\n\n## Search Results\n{example['search_context']}"
    messages = [
        {"role": "system", "content": SEARCH_REASONER_SYSTEM_PROMPT},
        {"role": "user", "content": user_content},
        {"role": "assistant", "content": example["teacher_response"]},
    ]
    return {"messages": messages}


def apply_chat_template(example: dict, tokenizer) -> dict:
    """Apply the tokenizer's chat template to produce a text field."""
    messages = example["messages"]
    try:
        text = tokenizer.apply_chat_template(
            messages, tokenize=False, add_generation_prompt=False
        )
    except Exception:
        text = tokenizer.apply_chat_template(
            messages, tokenize=False
        )
    return {"text": text}


